package se.kth.paint.model;

public class Position {
	
	private int mX, mY;

	public Position(int x, int y) {
		mX = x;
		mY = y;
	}

	public int getX() {
		return mX;
	}

	public int getY() {
		return mY;
	}
	
	
	
	
}
